#!/usr/bin/env bash

function install_pip() {
  echo '======================================================='
  echo "[LOG] START: $ pip install --upgrade $1"
  echo '======================================================='

  pip install --upgrade "$1"

  echo '======================================================='
  echo "[LOG] END: $ pip install --upgrade $1"
  echo '======================================================='

  echo '**********************************************************************'
}

install_pip pip

exit 0
