#!/usr/bin/env bash

echo '----------------------------------------------------------------'
echo 'インストール可能な Ruby のバージョン'
echo '----------------------------------------------------------------'
rbenv install -l | grep -E "^( *)(2\.|3\.)(/*)"

echo '----------------------------------------------------------------'
echo '現在インストールされている Ruby のバージョン'
echo '----------------------------------------------------------------'
rbenv versions | grep -E "^( |\*)*(2\.[7-9]\.|3\.)(.*)"

echo '----------------------------------------------------------------'
echo 'インストール可能な Python のバージョン'
echo '----------------------------------------------------------------'
pyenv install -l | grep -E "^(. )(3\.9\.[0-9][0-9]|3\.10|4\.|anaconda3-202)(.*)$"

echo '----------------------------------------------------------------'
echo '現在インストールされている Python のバージョン'
echo '----------------------------------------------------------------'
pyenv versions | grep -E "^( |\*)*(3\.9|3\.10|4\.|anaconda3-202)(.*)"

echo '----------------------------------------------------------------'
echo 'インストール可能な Terraform のバージョン'
echo '----------------------------------------------------------------'
tfenv list-remote | grep -E "^(1\.[1-9]*\.[0-9]*)$"

echo '----------------------------------------------------------------'
echo '現在インストールされている Terraform のバージョン'
echo '----------------------------------------------------------------'
tfenv list

echo '----------------------------------------------------------------'
echo 'インストール可能な PHP のバージョン'
echo '----------------------------------------------------------------'
phpenv install -l | grep -E "^(.*)(8\.1|8\.2)(.*)"

echo '----------------------------------------------------------------'
echo '現在インストールされている PHP のバージョン'
echo '----------------------------------------------------------------'
phpenv versions

echo '----------------------------------------------------------------'
echo 'インストール可能な Go のバージョン'
echo '----------------------------------------------------------------'
goenv install -l | tail -n 5

echo '----------------------------------------------------------------'
echo '現在インストールされている Go のバージョン'
echo '----------------------------------------------------------------'
goenv versions | tail -n 5

echo '----------------------------------------------------------------'
echo 'インストール可能な Node.js のバージョン'
echo '----------------------------------------------------------------'
nodenv install -l | grep -E "^( *)(16\.[0-9][0-9]\.|18\.|20\.)(.*)$"

echo '----------------------------------------------------------------'
echo '現在インストールされている Node.js のバージョン'
echo '----------------------------------------------------------------'
nodenv versions | grep -E "^( |\*)*(16\.|18\.|20\.)(.*)$"

exit 0
